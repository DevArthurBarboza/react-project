export default class Item {
    id : number;
    price : number;
    name : string;
    isAvailable : boolean;

    constructor (
        id : number,
        price : number,
        name : string,
        isAvailable : boolean
    ) {
        this.id = this.id;
        this.price = price;
        this.name = name;
        this.isAvailable = isAvailable;
    }
}